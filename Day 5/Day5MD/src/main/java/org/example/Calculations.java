public class Calculations {
    public static int saskaitit(int a, int b) {
        return a + b;
    }

    public static int atnemt(int a, int b) {
        return a - b;
    }

    public static int reizinat(int a, int b) {
        return a * b;
    }

    public static int dalit(int a, int b) {
        return a / b;
    }
}
